	rot = rot-90+180
	setPedRotation(thePlayer, rot)
	setPedAnimation(thePlayer, "ped", "ATM", -1, true, false, false)
	toggleAllControls(thePlayer, false)
	--executeCommandHandler("me", thePlayer, " durchsucht eine Muelltonne.")
	setTimer(function()
		setPedAnimation(thePlayer)
		toggleAllControls(thePlayer, true)
		dosuche[thePlayer] = false
		-- THE RANDOM THING --
		--local rand = math.random(1, 8)
		local rand = math.random(1, 9)
		
		if(rand == 1) or(rand == 2) then -- Geld
			local geld = math.random(10, 200)
			local money = vnxGetElementData (thePlayer, "money")
			vnxSetElementData (thePlayer, "money", money + geld)
			outputChatBox("Du hast "..geld.."$ in der Mülltonne gefunden!", thePlayer, 0, 255, 255)
		elseif(rand == 3) then -- Waffen
			local id = math.random(1, 4)
			if(id == 1) then id = 22 end -- 9mm
			if(id == 2) then id = 4 end -- Messer
			--if(id == 3) then id = 28 end -- Uzi
			if(id == 3) then id = 2 end -- Golfclub
			if(id == 4) then id = 6 end -- Schaufel
			local munni = math.random(17, 51)
			giveWeapon(thePlayer, id, munni, true)
			outputChatBox("Du hast ein(e) "..getWeaponNameFromID(id).." in der Mülltonne gefunden!", thePlayer, 0, 255, 255)
		elseif(rand == 5) or (rand == 6) then
			 local text = zufallitems[math.random(1, #zufallitems)]
			 outputChatBox("Du hast "..text.." in der Mülltonne gefunden, und wirfst sie/es/ihn weg.", thePlayer, 0, 255, 255)
		elseif(rand == 7) or (rand == 4) then
			outputChatBox("Du hast paar Materialien in der Mülltonne gefunden!", thePlayer, 0, 255, 255)
			vnxSetElementData ( thePlayer, "mats", vnxGetElementData ( thePlayer, "mats")+30)
		elseif(rand == 8) then
			outputChatBox("Du hast paar Drogen in der Mülltonne gefunden!", thePlayer, 0, 255, 255)
			vnxSetElementData ( thePlayer, "drugs", vnxGetElementData ( thePlayer, "drugs")+10)
		else
			outputChatBox("Du hast nichts in der Mülltonne gefunden.", thePlayer, 0, 255, 255)
		end
	end, 9000, 1)
end

addEventHandler("onResourceStart", getResourceRootElement(), function()
	for index, tonne in next, tonnen do
		local x, y, z = getElementPosition(tonne)
		mark[tonne] = createMarker(x, y, z, "corona", 2.5, 255, 255, 255, 50)
		addEventHandler("onMarkerHit", mark[tonne], function(hitElement)
			if(getElementType(hitElement) == "player") and (isPedInVehicle(hitElement) == false) then
				if(dosuche[hitElement] == true) then return end
				if(done[source] == true) then
					outputChatBox("Diese Mülltonne wurde bereits durchsucht. Hier findest du nichts.", hitElement, 255, 0, 0)
					return
				end
				if(isTimer(waitTimer[hitElement])) then return end
				local o = source
				waitTimer[hitElement] = setTimer(doMuelltonnenSuche, 1000, 1, hitElement, o)
			end
		end)
		addEventHandler("onMarkerLeave", mark[tonne], function(hitElement)
			if(getElementType(hitElement) == "player") and (isPedInVehicle(hitElement) == false) then
				if(isTimer(waitTimer[hitElement])) then killTimer(waitTimer[hitElement]) end
			end
		end)
	end
end)
